let friendsAtYourHome = 0; //Initialization: The variable friendsAtYourHome is initialized with a value of 0.

//While Loop: The while loop runs as long as the condition friendsAtYourHome < 10 is true.
while (friendsAtYourHome < 10){
    friendsAtYourHome= friendsAtYourHome+1; //Iteration
    // you can also write the above line as
    // friendsAtYourHome +=1; or friendsAtYourHome++; //this will return the same output 
}
console.log(friendsAtYourHome); //the output of this code snippet is 10 because the loop iterates f rom 0 to 9, incrementing the value of friendsAtYourHome each time, until it reaches 10.